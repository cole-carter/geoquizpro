export const getPopulationRange = (population) => {
  if (population < 10000000) return 'Under 10M';
  if (population < 50000000) return '10M - 50M';
  if (population < 100000000) return '50M - 100M';
  if (population < 500000000) return '100M - 500M';
  return 'Over 500M';
};

export const getRandomCountries = (count = 10, excludeIds = []) => {
  const available = countries.filter(c => !excludeIds.includes(c.id));
  const shuffled = [...available].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getCountryById = (id) => {
  return countries.find(c => c.id === id);
};

export const getCountriesByRegion = (region) => {
  return countries.filter(c => c.region === region);
};